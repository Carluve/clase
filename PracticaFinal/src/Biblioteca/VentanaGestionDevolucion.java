/**Boton realizar devolución */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        Calendar c1 = Calendar.getInstance();
        Calendar c2 = objpres.fecha_devolucion;
        long inicio = c1.getTimeInMillis();
        long devolucion = c2.getTimeInMillis();
        long resultado = inicio-devolucion;
        double dias = Math.floor(resultado/(1000*60*60*24));
        if (dias <=0){
         try{   
            GestionPublicacion.ticketDevolucion(objpres);   
            JOptionPane.showMessageDialog(this, "La publicación se ha devuelto dentro de plazo", "Devolución prestamo", JOptionPane.INFORMATION_MESSAGE);
         }
         catch(Exception IOE){
             System.out.println("Error en la salida: " + IOE.toString());
         }
        } 
        else{
        try{
            GestionPublicacion.ticketDevolucionSancion(objpres);
            JOptionPane.showMessageDialog(this, "Devolución con amonestación.\n Se ha excendido en " +dias +" dias.", "Baja publicaciones", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(Exception IOE){
            System.out.println("Error en la salida con sancion: " + IOE.toString());
        }
        }
        GestionPublicacion.devolucionPublicacion(objpres);
        GestionPublicacion.guardarDatos();
        GestionPublicacion.guardarDatosPrestamo();
        VentanaGestionPublicaciones vgp = new VentanaGestionPublicaciones();
        vgp.setVisible(true);
        this.dispose();

    }//GEN-LAST:event_jButton2ActionPerformed

