     private void consultaPrestamo() {
        try {
            /**Referenciamos al ArrayList de GestionPublicaciones (Prestamo)*/
           prestamos = GestionPublicacion.getPrestamo();
            /** Creamos el iterador sobre el ArrayList*/
                li = prestamos.listIterator();
                int codigo = 0;
           //Si no quedan libros prestamos
            if (prestamos.size() < 1) {
                JOptionPane.showMessageDialog(this, "No hay mas publicaciones.", "Gestion publicaciones", JOptionPane.INFORMATION_MESSAGE);
                jButton3.setEnabled(false);
                jButton4.setEnabled(false);
                return;
            } else {
                jButton3.setEnabled(true);
                jButton4.setEnabled(true);
                }
            //presentamos 
            if (li.hasNext()) {
                objpres = (Prestamo) li.next();
            }
            if (objpres != null) {
                presenta(objpres);
            } else {
                JOptionPane.showMessageDialog(this, "No hay mas prestamos.", "Gestion prestamos", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Mensaje", JOptionPane.ERROR_MESSAGE);
            System.out.println("Error: " + e.toString());
            return;
        }
    }//fin consultaPrestamos

    /** Método para presentar los datos*/
